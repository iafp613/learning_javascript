// OPERADORES DE ASIGNACIÓN
// 1. Asignación (=)

var x = 5;
console.log(x); // 5

//2. Asignación de múltiples variables

var y=5, z=10;
console.log(y, z); // 5 10

// Asignación de adición (+=)

var x = 5
var y = 2
console.log(x += y); // 7

//Asinación de resta (-=)
var x = 5
var y = 2
console.log(x -= y); // 3

//Asignación de multiplicación (*=)

var x = 5
var y = 2
console.log(x *= y); // 10

//Asignación de división (/=)

var x = 5
var y = 2
console.log(x /= y); // 2.5

//Asignación de módulo (%)

var x = 5
var y = 2
console.log(x %= y); // 1

//Asignación de exponenciación (**=)

var x = 5
var y = 2
console.log(x **= y); // 25

//Asignación de concatenación (+=)

var x = "Hola "
var y = "Mundo"
console.log(x += y); // Hola Mundo